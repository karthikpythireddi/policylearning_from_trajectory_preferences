"""
datasets_preference.py

PyTorch Dataset for loading preference pairs from HDF5 files generated by
scripts/collect_preferences.py.

Each item yields a (winner_window, loser_window) pair where each window is
seq_len consecutive timesteps sampled from the full trajectory.

Image preprocessing:
  HDF5 stores:  (T, H, W, C)  uint8   channels-last
  Policy expects: (T, C, H, W)  float32  channels-first, normalized to [0, 1]

Proprioception preprocessing:
  HDF5 stores:  (T, dim)  float64
  Policy expects: (T, dim)  float32
"""

import h5py
import numpy as np
import torch
from torch.utils.data import Dataset


class PreferenceWindowDataset(Dataset):
    """
    Loads preference pairs from a single task's HDF5 file.

    For each pair, samples a random window of seq_len timesteps from both
    the winner and loser trajectories independently.

    Args:
        hdf5_path:         Path to task{i}_preferences.hdf5
        task_emb:          torch.Tensor of shape (768,) — BERT task embedding
        seq_len:           Number of timesteps per window (must match policy's
                           transformer_max_seq_len, typically 10)
        n_windows_per_pair: How many random windows to sample per pair.
                            Total dataset size = n_pairs × n_windows_per_pair
    """

    def __init__(self, hdf5_path, task_emb, seq_len=10, n_windows_per_pair=5):
        self.hdf5_path = hdf5_path
        self.seq_len = seq_len

        # Store task embedding as numpy for easy collation
        if isinstance(task_emb, torch.Tensor):
            self.task_emb = task_emb.numpy().astype(np.float32)
        else:
            self.task_emb = np.array(task_emb, dtype=np.float32)

        # Read pair metadata and pre-sample window start indices
        # We do this once at init so indices are fixed (reproducible)
        self.index = []  # list of (pair_idx, winner_start, loser_start)

        with h5py.File(hdf5_path, "r") as f:
            n_pairs = f["metadata"].attrs["n_pairs"]

            for pair_idx in range(n_pairs):
                grp = f[f"pair_{pair_idx}"]
                T_w = grp.attrs["winner_length"]
                T_l = grp.attrs["loser_length"]

                # Skip pairs where trajectory is too short for a window
                if T_w < seq_len or T_l < seq_len:
                    continue

                for _ in range(n_windows_per_pair):
                    w_start = np.random.randint(0, T_w - seq_len + 1)
                    l_start = np.random.randint(0, T_l - seq_len + 1)
                    self.index.append((pair_idx, w_start, l_start))

    def __len__(self):
        return len(self.index)

    def _load_window(self, f, pair_idx, role, start):
        """
        Load seq_len timesteps from a winner or loser trajectory.

        Returns:
            obs dict with keys:
                agentview_rgb:   (seq_len, C, H, W)  float32  [0, 1]
                eye_in_hand_rgb: (seq_len, C, H, W)  float32  [0, 1]
                joint_states:    (seq_len, 7)         float32
                gripper_states:  (seq_len, 2)         float32
            actions: (seq_len, 7) float32
        """
        grp = f[f"pair_{pair_idx}/{role}"]
        end = start + self.seq_len

        actions = grp["actions"][start:end].astype(np.float32)  # (T, 7)

        obs = {}

        # RGB images: uint8 (T, H, W, C) → float32 (T, C, H, W) normalized
        for key in ["agentview_rgb", "eye_in_hand_rgb"]:
            if key in grp["obs"]:
                img = grp[f"obs/{key}"][start:end]          # (T, H, W, C) uint8
                img = img.astype(np.float32) / 255.0        # normalize to [0, 1]
                img = img.transpose(0, 3, 1, 2)             # (T, C, H, W)
                obs[key] = img

        # Proprioception: float64 → float32
        for key in ["joint_states", "gripper_states"]:
            if key in grp["obs"]:
                obs[key] = grp[f"obs/{key}"][start:end].astype(np.float32)

        return {"obs": obs, "actions": actions}

    def __getitem__(self, idx):
        pair_idx, w_start, l_start = self.index[idx]

        with h5py.File(self.hdf5_path, "r") as f:
            winner = self._load_window(f, pair_idx, "winner", w_start)
            loser  = self._load_window(f, pair_idx, "loser",  l_start)

        return {
            "winner":   winner,
            "loser":    loser,
            "task_emb": self.task_emb.copy(),  # (768,) float32
        }


def make_preference_dataloader(hdf5_path, task_emb, seq_len=10,
                                n_windows_per_pair=5, batch_size=16,
                                shuffle=True):
    """
    Convenience function: creates a DataLoader from a preference HDF5 file.
    Always uses num_workers=0 to avoid h5py pickling issues.
    """
    from torch.utils.data import DataLoader

    dataset = PreferenceWindowDataset(
        hdf5_path=hdf5_path,
        task_emb=task_emb,
        seq_len=seq_len,
        n_windows_per_pair=n_windows_per_pair,
    )
    loader = DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=shuffle,
        num_workers=0,   # h5py cannot be pickled across worker processes
    )
    return loader, dataset
